export async function seedIndustrialComparison() {
  try {
    // Check if comparison data already exists
    const [sample] = await db
      .select({ comparison: complianceChecklist.industrialRegComparison })
      .from(complianceChecklist)
      .where(sql`${complianceChecklist.industrialRegComparison} IS NOT NULL AND ${complianceChecklist.industrialRegComparison} != ''`)
      .limit(1);

    if (sample) {
      // Already seeded
      return;
    }

    let updated = 0;
    for (const comp of COMPARISONS) {
      await db.update(complianceChecklist)
        .set({
          industrialRegComparison: comp.industrialRegComparison,
          industrialRegWhyDifferent: comp.industrialRegWhyDifferent,
          industrialRegApproach: comp.industrialRegApproach,
          updatedAt: new Date(),
        })
        .where(sql`${complianceChecklist.title} LIKE ${'%' + comp.titleMatch + '%'}`);

      updated++;
    }

    console.log(`[startup] Industrial regulation comparison data seeded for ${updated} checklist items`);
  } catch (err) {
    console.error('[startup] Industrial comparison seed failed (non-fatal):', err);
  }
}
